/* 
  Micro-optimization to alter the palette to friendly to the typed array within the
  2D canvas context.
*/
for (let i = 0; i < palette.length; i++) {
  const color = palette[i];

  palette[i] =
    0xff000000 |
    ((color & 0x0000ff) << 16) |
    (color & 0x00ff00) |
    (color >>> 16);
}

export const PpuPalette = palette;